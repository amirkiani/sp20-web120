<h2>Summary</h2>
		
        <p>One way of integrating images to a website is using galleries such as embedding a simple Lightbox gallery to your website and implementing it via HTML. Another even simpler way is to use a CMS such as Wordpress or TYPO3 (How to create a photo gallery for your website 2016) . For Lightbox, you will first need to install Lightbox, embed your JS and CSS, and then use the Lightbox attribute for embedding in HTML.
</p>
        <p>You can use plugins for Wordpress such as NextGen Gallery, Envira Gallery, and Polaroid Gallery. There are other plugins for other content management systems. For Joomla there is sigplus, phoca gallery, and Simple Image Gallery Extended (SIGE). Galleries for TYPO3 include Yet Another Gallery and Perfect Lightbox (How to create a photo gallery for your website 2016).

</p>
<p>Another way to use photo galleries is to make a Polaroid Photo Gallery in HTML and CSS (Kunal 2018). First link your css folder in your html. Then download your images and place them in your images folder. Then use figure and figcaption in your HTML. Style your CSS for figure and figcaption. Use a div class for your gallery. Use flexbot in your CSS to center your images. Then use CSS to style your images to look like a Polaroid. Use flexwrap to give yourself another row for your fifth image. Then use styling to add margin and background color (Kunal 2018).

</p>

        
        
        
