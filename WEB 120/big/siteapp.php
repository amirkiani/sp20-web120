<h2>Summary</h2>
		
        <p>Nearly every business has a website or app (Cherednichenko 2019). You can have both a responsive website and app but sometimes if your budget and time is limited, you might have to choose just building a responsive website since it is faster and cheaper. It also depends on your industry, as mobile websites are prefered for searching, book, and purchasing while mobile apps are favored for entertainment and communications (Garbar 2020). 51% of all traffic is via mobile devices so that is an indicator you should have a mobile app (Cherednichenko 2019). </p>
        <p>The pros of the mobile apps are that it is functional and flexible. It also has the advantage of using your smartphone’s features such as camera, geolocation, and push notifications which increases engagement. It is also more secure and faster (Cherednichenko 2019). Mobile apps are also available offline and use geofencing which also increases engagement (Garbar 2020).</p>
<p>The cons of mobile apps are that it is harder to acquire users and it is also more time-consuming and expensive (Cherednichenko 2019). Websites are easier to maintain and can be updated immediately while mobile apps need to be updated constantly and take days to be reviewed by Apple or Google (Garbar 2020). The pros of a website is that it is cheaper to develop, faster and works on platforms (Cherednichenko 2019). It is also easier to build an audience on a website since people do not need to install an app on a website (Garbar 2020). Mobile web is good for initial awareness and engagement. A con of a website is that it needs constant internet connections. 
</p>
<p>It is not black and white issue and the decision should be made based on your business’ goals and resources. Apps give a more personalized experience but responsive websites can attract a larger audience and are much more affordable. (Garbar 2020).
</p>

        
        
        
